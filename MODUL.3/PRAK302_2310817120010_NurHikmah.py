nilai = int(input('Input\n'))

if nilai >= 80 and nilai <= 100:
    print('\nOutput \nA')
elif nilai >= 70 and nilai <= 79:
    print('\nOutput \nB')
elif nilai >= 60 and nilai <= 69:
    print('\nOutput \nC')
elif nilai >= 50 and nilai <= 59:
    print('\nOutput \nD')
else:
    print('\nOutput \nE')